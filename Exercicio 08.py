n = int(input('Digite uma quantidade de metros: '))
km = n / 1000
hm = n / 100
dam = n / 10
dm = n * 10
cm = n * 100
mm = n * 1000

print('A quantidade de metros que voce escolheu equivalem:')
print(f'{km} Km')
print(f'{hm} Hm')
print(f'{dam} Dam')
print(f'{dm} Dm')
print(f'{cm} Cm')
print(f'{mm} Mm')
